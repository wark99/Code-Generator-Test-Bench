public class Comment {
    private Long id;
    private String content;

    // Constructor
    public Comment(Long id, String content) {
        this.id = id;
        this.content = content;
    }

    // Getters and Setters
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }
}

